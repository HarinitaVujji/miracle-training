"""Take a user’s name and print:
Name in uppercase
Name in lowercase
Length of the name"""
#take user name
users_name = "Harinita"
# convert to upper case,lower case and calculate length
upper_case_name = users_name.upper()
lower_case_name = users_name.lower()
length_of_username = len(users_name)
#print results
print("User's name in upper case: " , upper_case_name)
print("User's name in lower case: ", lower_case_name)
print("Length of the user's name: ", length_of_username)
